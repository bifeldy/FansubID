function getRandomInt(min: number, max: number): number {
  min = Math.ceil(min);
  max = Math.floor(max);
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

export function getQuizHirakata(min = 0, max = listQuestion.length - 1) {
  const randomInteger = getRandomInt(min, max);
  if (randomInteger < 2) {
    return {
      randomInteger,
      question: listQuestion[randomInteger],
      options: [
        listQuestion[0],
        listQuestion[1],
        listQuestion[2],
        listQuestion[3],
        listQuestion[4]
      ]
    };
  } else if (randomInteger > listQuestion.length - 3) {
    return {
      randomInteger,
      question: listQuestion[randomInteger],
      options: [
        listQuestion[listQuestion.length - 5],
        listQuestion[listQuestion.length - 4],
        listQuestion[listQuestion.length - 3],
        listQuestion[listQuestion.length - 2],
        listQuestion[listQuestion.length - 1]
      ]
    };
  } else {
    return {
      randomInteger,
      question: listQuestion[randomInteger],
      options: [
        listQuestion[randomInteger - 2],
        listQuestion[randomInteger - 1],
        listQuestion[randomInteger],
        listQuestion[randomInteger + 1],
        listQuestion[randomInteger + 2]
      ]
    };
  }
}

